"""Generate synthetic SD74 GDE input files for regression testing.

Produces realistic-looking but entirely fake data with the same column
schema as real SD74 GDE exports. Run this script once to regenerate
the snapshot input files (e.g. after a schema change):

    python tests/snapshots/generate_synthetic.py

The output (tests/snapshots/input/) is committed to git.
The golden output (tests/snapshots/output/) is regenerated by running:

    python -c "
    from pathlib import Path
    from src.main import main
    main('sd74myedbc', 'tests/snapshots/input', 'tests/snapshots/output')
    "
"""

