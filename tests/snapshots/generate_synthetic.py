def _pad(n: int, width: int = 7) -> str:
    return str(n).zfill(width)


def _dob(grade: str) -> str:
    age = {
        "K": 5,
        "1": 6,
        "2": 7,
        "3": 8,
        "4": 9,
        "5": 10,
        "6": 11,
        "7": 12,
        "8": 13,
        "9": 14,
        "10": 15,
        "11": 16,
        "12": 17,
    }.get(grade, 10)
    year = 2025 - age
    month = random.randint(1, 12)
    day = random.randint(1, 28)
    return f"{year}-{month:02d}-{day:02d}"


