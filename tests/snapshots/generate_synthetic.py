def _write(filename: str, header: str, rows: list[str]) -> None:
    path = OUTPUT_DIR / filename
    with open(path, "w", encoding="utf-8", newline="\n") as f:
        f.write(header + "\n")
        for row in rows:
            f.write(row + "\n")
    print(f"  {filename}: {len(rows)} rows")


